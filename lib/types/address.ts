export interface Address {
  id: string
  type: 'shipping' | 'billing' | 'both'
  name: string
  company?: string | null
  addressLine1: string
  addressLine2?: string | null
  city: string
  state: string
  zipCode: string
  country: string
  phone?: string | null
  isDefault: boolean
  createdAt: string
  updatedAt: string
}

export interface CreateAddressRequest {
  type?: 'shipping' | 'billing' | 'both'
  name: string
  company?: string
  addressLine1: string
  addressLine2?: string
  city: string
  state: string
  zipCode: string
  country?: string
  phone?: string
  isDefault?: boolean
}

export interface UpdateAddressRequest {
  type?: 'shipping' | 'billing' | 'both'
  name?: string
  company?: string
  addressLine1?: string
  addressLine2?: string
  city?: string
  state?: string
  zipCode?: string
  country?: string
  phone?: string
  isDefault?: boolean
}

export interface AddressResponse {
  success: boolean
  data: Address
  message?: string
}

export interface AddressListResponse {
  success: boolean
  data: Address[]
}

export interface DeleteAddressResponse {
  success: boolean
  message: string
}

